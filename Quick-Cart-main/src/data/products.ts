export const getProductById = (id: string): Product | undefined => {
  return products.find(product => product.id === id);
};

export const getProductsByCategory = (category: string): Product[] => {
  return products.filter(product => product.category.toLowerCase() === category.toLowerCase());
};

export const getRelatedProducts = (productId: string, limit: number = 4): Product[] => {
  const currentProduct = getProductById(productId);
  
  if (!currentProduct) return [];
  
  return products
    .filter(product => 
      product.id !== productId && 
      product.category === currentProduct.category
    )
    .slice(0, limit);
};

export const getNewArrivals = (limit: number = 4): Product[] => {
  // In a real app, this would sort by date or use a "new" flag
  // For this demo, we'll just take the first few products
  return products.slice(0, limit);
};

export const getFeaturedProducts = (limit: number = 4): Product[] => {
  // For demo purposes, just returning some products
  return products.slice(3, 3 + limit);
};
